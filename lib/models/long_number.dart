class LongNumber {
  String number;
  int maxdays;
  int days;
  LongNumber({required this.number, required this.maxdays, required this.days});
}

class TripleNumber {
  String number;
  List<String> items;  
  TripleNumber({required this.number, required this.items});
}

class MatchNumber {
  String number;  
  int days;
  MatchNumber({required this.number, required this.days});
}